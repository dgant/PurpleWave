package Mathematics

import Mathematics.Functions._

object Maff
  extends Angles
    with  Approximate
    with  Bitshift
    with  Convert
    with  Polygons
    with  Rectangles
    with  Reduce
    with  Sample
    with  Sort {
  val inv8    : Double = 1 / 8.0
  val inv16   : Double = 1 / 16.0
  val inv32   : Double = 1 / 32.0
  val inv64   : Double = 1 / 64.0
  val inv128  : Double = 1 / 128.0
  val inv256  : Double = 1 / 256.0
}
